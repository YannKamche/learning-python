# operators: Symbols used to perform operations on 
#            values and the variables that hold those values.

# A program to display whether a day will be sunny or rainy based on the 
# temperature
temperature = 24;

# Conditional operator in Python
if temperature < 30 :
    print("It's a rainy day");
else:
    print("It is a sunny day");
    
print('');

# The ternary operator in Python
print("It will rain") if temperature < 30 else print("It will not rain");